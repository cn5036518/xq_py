def yue():  #1定义函数  函数的定义：函数是对功能或者动作的封装
    print('打开手机')
    print('打开陌陌')
    print('找到心仪的对象')
    print('出来吃吃饭')
    print('跳跳广场舞')

yue()   #2第一次调用函数  可以在任何时间，任何地点进行调用
print('回家休息休息，吃个西瓜')
yue()   #3第二次调用函数

