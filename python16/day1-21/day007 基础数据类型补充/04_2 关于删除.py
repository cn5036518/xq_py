li1 = ["张无忌", "张三丰", "张翠山", "张嘉译", '刘嘉玲', "刘能", '刘老根']
# # 删除列表中姓张的人

"""
思路：
1、循环遍历列表1，将姓张的人，添加到列表2   #条件判断  以张开头
2、循环遍历列表2的同时
    从列表1中进行删除
"""
li2 = []
for i in li1:  #1遍历循环列表1
    if i.startswith("张"):
        li2.append(i)  #将符合条件的元素依次添加到列表2
print(li2)  #['张无忌', '张三丰', '张翠山', '张嘉译']

for i in li2:  #1遍历循环列表2的同时   关键点
    li1.remove(i) #2从列表1中进行删除
print(li1)  #['刘嘉玲', '刘能', '刘老根']
















