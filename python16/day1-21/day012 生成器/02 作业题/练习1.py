# 2，用列表推导式做下列小题
# (1)	过滤掉长度小于3的字符串列表，并将剩下的转换成大写字母
li1 = ['jack','tom','bob','an']
li2 = [i.upper() for i in li1 if len(i)>=3]
print(li2)  #['JACK', 'TOM', 'BOB']
print('---------------1')

# (2)	求(x,y)其中x是0-5之间的偶数，y是0-5之间的奇数组成的元组列表
#方法1
li3 = [(x,y) for x in range(6) if x%2==0 for y in range(6) if y%2==1]
print(li3) #[(0, 1), (0, 3), (0, 5), (2, 1), (2, 3), (2, 5), (4, 1), (4, 3), (4, 5)]
#关键点：读懂题目，知道最后正确答案的形式是列表中包含元组，元组的元素1是偶数，元素2是奇数
