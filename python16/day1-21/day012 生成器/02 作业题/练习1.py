# (3)	求M中3,6,9组成的列表M = [[1,2,3],[4,5,6],[7,8,9]]
#方法1  从[[1,2,3],[4,5,6],[7,8,9]]  得到[3,6,9]
M = [[1,2,3],[4,5,6],[7,8,9]]
li4 = [i[-1] for i in M ]
print(li4)  #[3, 6, 9]
print('---------------3')

#方法2  从[3,6,9] 得到 [[1,2,3],[4,5,6],[7,8,9]]
li1 = [3,6,9]
li32 = [[i-2,i-1,i] for i in li1]  #直接遍历列表即可（不需要索引号取值）
print(li32)  #[[1, 2, 3], [4, 5, 6], [7, 8, 9]]
#关键点：找出规律
print('---------------3-2')














